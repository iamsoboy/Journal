                                    Disscussion:
                                    Interpret your data in the discussion in appropriate depth. This means that when you
                                    explain a phenomenon you must describe mechanisms that may account for the observation.
                                    If your results differ from your expectations, explain why that may have happened.
                                    If your results agree, then describe the theory that the evidence supported.
                                    It is never appropriate to simply state that the data agreed with expectations,
                                    and let it drop at that.
                                </p>
                                <ul class="list-disc">
