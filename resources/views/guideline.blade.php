                                    The Introduction should be concise (double spaced, typed).
                                    Methods:
                                    The methods section of a research paper provides the information by which a study’s validity is judged.
                                    Therefore, it requires a clear and precise description of how an experiment was done, and the rationale
                                    for why specific experimental procedures were chosen.
                                    The methods section structure should: describe the materials used in the study,
                                    explain how the materials were prepared for the study, describe the research protocol,
                                    explain how measurements were made and what calculations were performed,
                                    and state which statistical tests were done to analyze the data.
                                    This section should contain sufficient detail so that all experimental procedures
                                    can be repeated by others in conjunction with cited references. Procedural detail that has
                                    been published previously should be referred to by citation. When a modified procedure is used,
                                    only the author’s modifications of the previously published method need to be given in detail.
                                    Subheadings should be used.
